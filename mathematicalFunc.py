# abs() function
print("abs(-45) :", abs(-45))

# ceil() function
import math
print("math.ceil(10.25) :", math.ceil(10.25))
print("math.ceil(-30.01) :", math.ceil(-30.01))
print("math.ceil(math.pi) :", math.ceil(math.pi))

# floor() function
print("math.floor(10.25) :", math.floor(10.25))
print("math.floor(-30.01) :", math.floor(-30.01))
print("math.floor(math.pi) :", math.floor(math.pi))

# exp() function
print("math.exp(5) :", math.exp(5))
print("math.exp(-30.01) :", math.exp(-30.01))
print("math.exp(math.pi) :", math.exp(math.pi))


# log() function
print("math.log(100) :", math.log(100))
print("math.log(math.pi) :", math.log(math.pi))

# log10() function
print("math.log10(100) :", math.log10(100))
print("math.log10(math.pi) :", math.log10(math.pi))

# max() function
print("max(12, 15, 8) :", max(12, 15, 8))
print("max(100, 80, -10) :", max(100, 80, -10))

# min() function
print("min(12, 15, 8) :", min(12, 15, 8))
print("min(100, 80, -10) :", min(100, 80, -10))

# pow() function
print("math.pow(10, 2)", math.pow(10, 2))
print("math.pow(100, -2)", math.pow(100, -2))

# round() function
print("round(70.23457)", round(70.23457))
print("round(70.23457, 2)", round(70.23457, 2))
print("round(111.261, 1)", round(111.261, 1))
print("round(-107.52)", round(-107.52))

# sqrt() function
print("math.sqrt(100)", math.sqrt(100))
print("math.sqrt(24.5)", math.sqrt(24.5))